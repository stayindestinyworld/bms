        public static string CheckFileType(string fileinfo)
        {
            string file = "Unknown";
            fileinfo = fileinfo.ToLower();
            if (fileinfo.Contains(".sql"))
            {
                file = "SQL file";
            }
            else if (fileinfo.Contains(".doc"))
            {
                file = "Word file";
            }
            else if (fileinfo.Contains(".xls"))
            {
                file = "Excel file";
            }
            else if (fileinfo.Contains(".pdf"))
            {
                file = "PDF file";
            }
            else if (fileinfo.Contains(".exe"))
            {
                file = "System file";
            }
            else if (fileinfo.Contains(".rar"))
            {
                file = "Winrar file";
            }
            else if (fileinfo.Contains(".zip"))
            {
                file = "Zip file";
            }
            else if (fileinfo.Contains(".txt"))
            {
                file = "Text file";
            }
            else if (fileinfo.Contains(".ppt"))
            {
                file = "Power Point file";
            }
            else if (fileinfo.Contains(".gif") || fileinfo.Contains(".jpg") || fileinfo.Contains(".jpe") || fileinfo.Contains(".png") || fileinfo.Contains(".bmp") || fileinfo.Contains(".tif"))
            {
                file = "Image file";
            }
            else if (fileinfo.Contains(".msg"))
            {
                file = "Outlook file";
            }
            else if (fileinfo.Contains(".cg"))
            {
                file = "Libable file";
            }
            else
            {
                file = "Unknown";
            }
            return file;
        }
